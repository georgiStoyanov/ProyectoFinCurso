package modelos;

public class SubCategoria {
	private int id_sub_categoria;
	private String nombre_sub_categoria;
	private String categoria;
	public int getId_sub_categoria() {
		return id_sub_categoria;
	}
	public void setId_sub_categoria(int id_sub_categoria) {
		this.id_sub_categoria = id_sub_categoria;
	}
	public String getNombre_sub_categoria() {
		return nombre_sub_categoria;
	}
	public void setNombre_sub_categoria(String nombre_sub_categoria) {
		this.nombre_sub_categoria = nombre_sub_categoria;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
}
